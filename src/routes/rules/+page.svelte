		</p>
		<p>
			● <b>En cas de prolongations</b>, le résultat du pronostic est obtenu à partir du score <b>à la fin des 120 minutes</b> et du temps additionnel.
			<br /> La séance de tir au but n'est pas prise en compte.
		</p>
		<p>
			● Pour les <b>matchs de poules</b>, un pronostic <b>partiel</b> octroie <b>8 points</b>, tandis qu'un pronostic <b>exact</b> octroie <b>16 points</b>
		</p>
		<p>
			● Pour les <b>huitièmes de finale</b>, un pronostic <b>partiel</b> octroie <b>12 points</b>, tandis qu'un pronostic <b>exact</b> octroie <b>24 points</b>
		</p>
		<p>
			● Pour les <b>quarts de finale</b>, un pronostic <b>partiel</b> octroie <b>16 points</b>, tandis qu'un pronostic <b>exact</b> octroie <b>32 points</b>
		</p>
		<p>
			● Pour les <b>demi-finales</b>, un pronostic <b>partiel</b> octroie <b>20 points</b>, tandis qu'un pronostic <b>exact</b> octroie <b>40 points</b>
		</p>
		<p>
			● Pour la <b>finale</b>, un pronostic <b>partiel</b> octroie <b>30 points</b>, tandis qu'un pronostic <b>exact</b> octroie <b>60 points</b>
		</p>
		<p>
			● <b>Pour chaque match, le nombre de points attribués est ensuite multiplié par la côte du résultat match</b> (i.e. le nombre de points octroyé est pondéré par la difficulté du résultat trouvé).
		</p>
